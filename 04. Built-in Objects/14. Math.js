/*
Math é um objeto com métodos matemáticos

Podemos saber o valor de PI e gerar números aleatórios com Math */

console.log(Math.PI); // 3.141592653589793
console.log(Math.random()); // número aleatório entre 0 e 1

console.log(Math.floor(Math.random() * 10)); // número inteiro aleatório entre 0 e 10

console.log(Math.LN2); // 0.6931471805599453 - logaritmo natural de 2
console.log(Math.LN10); // 2.302585092994046 - logaritmo natural de 10
